/**
 * HotelBookingApp
 *
 * Entry point for the Hotel Booking System application.
 * This class demonstrates how a Java application starts execution
 * and displays a welcome message to the user through the console.
 *
 * The program prints the application name and version information
 * when it is launched.
 *
 * @author Kaustubh Chauhan
 * @version 1.0
 */
public class BookMyStayApp {

    /**
     * Main method - starting point of the Java application.
     * The JVM invokes this method when the program is executed.
     *
     * @param args Command line arguments (not used in this program)
     */
    public static void main(String[] args) {

        // Print welcome message
        System.out.println("Welcome to the Hotel Booking System!");

        // Print application name and version
        System.out.println("Application: Hotel Booking System");
        System.out.println("Version: v1.0");

        // Inform user that the application has started successfully
        System.out.println("Application started successfully.");
    }
}